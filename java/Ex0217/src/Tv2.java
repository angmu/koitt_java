
public class Tv2 {

	//Tv2를 만들고
	//volume 변수를 만들고 
	//main 메소드에서 변수에 10을 저장해보세오
	//volume Up Down을 만들어보세오
	
	int volume;
	
	void volumeUp() {
		System.out.println("볼륨을 1증가합니다");
		volume++;
		System.out.println("현재볼륨 :"+volume);
	}
	
	void volumeDown() {
		System.out.println("볼륨을 1감소합니다");
		volume--;
		System.out.println("현재볼륨 :"+volume);
	}
	
	
	
}
